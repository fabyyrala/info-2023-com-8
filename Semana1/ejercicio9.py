"""Escribe un programa que solicite al usuario dos números y luego imprima la
suma, la resta, la multiplicación y la división de los dos números.
"""

num1=float(input('Ingrese el primer numero:'))
num2=float(input('Ingrese el segundo numero:'))

print(f'La SUMA de los numeros da como resultado: {num1+num2}')
print(f'La RESTA entre los numeros da como resultado : {num1-num2}')
print(f'La MULTIPLICACION entre los numeros da como resultado: {num1*num2}')
print(f'La DIVISION entre los numeros da como resultado: {num1/num2} ')